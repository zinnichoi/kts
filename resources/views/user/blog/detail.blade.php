                            <h1>20 điều cần biết để tránh khi thiết kế và thi công nội thất</h1>

                            <div class="singlePostMeta">
                                <time class="postTime" datetime="07/12/2017">Ngày đăng: 07/12/2017</time>
                            </div>
                        </div>
                        <div class="desc-blog-post content-standard-ck">
                            <p>Ngày nay, sự thần bí của phong thuỷ vẫn còn tồn tại trong mỗi chúng ta. Trong bản tóm tắt này, phong thuỷ nghiên cứu môi trường dẫn đến sự hài hoà trong cuộc sống xung quanh quý khách qua việc cân bằng âm dương nhằm tránh được những sự rắc rối trong cuộc sống xung quanh bạn</p>

                            <p style="text-align:center"><img alt="" height="520" src="http://w3ni601.web3nhat.net/data/media/718/files/thiet-ke-noi-that-phong-khach-chung-cu-dep.jpg" width="800" /></p>

                            <section>
                                <p>1. Đặt giá sách trong phòng ngủ</p>

                                <p>Hậu quả: Hành động này như những mũi tên độc toả ra nhiều âm khí</p>

                                <p>2. Đầu giường không được kê sát vào tường.<br />
                                    Hậu quả: Đây là một nguyên nhân gây nên bệnh đau đầu</p>

                                <p>3. Kê giường dưới quạt<br />
                                    Hậu quả: Làm cho bạn bị hút âm khí</p>

                                <p>4. Đặt giường ở giữa phòng (2 đầu đều không được kê sát vào tường)<br />
                                    Hậu quả: Điều này giống như việc làm cho bạn bị trôi nổi trong không trung mà không có điểm tựa. Đây là điều tối kỵ.</p>

                                <p>5. Đầu giường kê đối diện với cửa ra vào<br />
                                    Hậu quả: Làm cho bạn bị suy sụp về sức khoẻ và luôn khiến bạn cảm thấy mệt mỏi</p>

                                <p>6. Ngủ trên sàn nhà<br />
                                    Hậu quả: Làm cho âm khí tăng lên, đó là nguyên nhân của những rủi ro và đau ốm</p>

                                <p>7. Đầu giường ngủ bị xà ngang đè lên (xà ngang ép đỉnh)<br />
                                    Hậu quả: Ảnh hưởng không tốt đến sức khoẻ, là nguyên nhân của bệnh đau đầu và đau nửa đầu</p>

                                <p>8. Giường ngủ bị gương chiếu thẳng vào<br />
                                    Hậu quả: Phản chiếu lại khí phát ra từ cơ thể bạn trong khi ngủ, khiến bạn suy sụp về sức khoẻ và công việc.</p>

                                <p>9. Đặt hoa mẫu đơn trong phòng ngủ không đúng hướng Tây-nam<br />
                                    Hậu quả: Nguyên nhân của sự không chung thuỷ trong cuộc sống vợ chồng.</p>

                                <p>10. Đặt tivi đối diện với giường ngủ<br />
                                    Hậu quả: Từ trường phát ra từ ti vi sẽ ảnh hưởng không tốt đến sức khoẻ của bạn.</p>

                                <p>11. Tập trung quá nhiều người trong phòng ngủ<br />
                                    Hậu quả: Ngăn chặn sự lưu thông của không khí</p>

                                <p>12. Những con số 2, 4 và 5<br />
                                    Hậu quả: Đây là những con số thường mang đến bệnh tật và ốm đau.</p>

                                <p>13. Đặt những tấm bình phong theo hình chữ chi<br />
                                    Hậu quả: Tạo quá nhiều góc nhọn sẽ làm toả ra nhiều sát khí</p>

                                <p>14. Ngồi ăn ở nơi góc nhọn hoắt của bàn ăn hình vuông, hình chữ nhật.<br />
                                    Hậu quả: Ảnh hưởng của nó như những mũi tên độc chĩa về phía bạn.</p>

                                <p>15. Đặt hoa khô trong nhà<br />
                                    Hậu quả: Những bông hoa khô toả ra rất nhiều âm khí.</p>

                                <p>16. Treo những chiếc đèn chùm có 5 bóng<br />
                                    Hậu quả: 5 bóng đèn tượng trưng cho âm khí.</p>

                                <p>17. Đặt bếp cạnh chậu rửa<br />
                                    Hậu quả: Thủy hoả xung khắc</p>

                                <p>18. Đặt bếp đối diện với cửa ra vào chính<br />
                                    Hậu quả: Cửa nhìn thẳng vào bếp nấu sẽ dẫn khí từ ngoài xộc thẳng vào, không có lợi, gây mất mát</p>

                                <p>19. Xà ngang đi qua bếp<br />
                                    Hậu quả: Hại cho sức khoẻ vợ chồng, là nguyên nhân của sự mất mát về tài chính</p>

                                <p>20. Đặt bếp đối diện tới cửa nhà vệ sinh<br />
                                    Hậu quả: Hại đến sức khoẻ, tạo nên sự xung khắc giữa lửa và nước.</p>

                                <p>Tất cả những điều trên được nghiên cứu nhằm tìm kiếm sự cân bằng trong môi trường sống của bạn. Cho dù những điều này không thuyết phục bạn, bạn hãy nghĩ lại những vấn đề của phong thuỷ như là sự kiểm chứng của cuộc sống được chắt lọc từ xa xưa để đem lại điều tốt lành cho người sử dụng nó.</p>
